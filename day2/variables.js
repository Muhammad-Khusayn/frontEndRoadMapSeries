/* 

3 ways of writing variable in js:

1)

var variabName = value;

2)

let variableName = value;

3)

const variableName = value;

*/

var varVariable = 10;

var varVariable = 12;

// var variable can be reassigned and incremented (this way of assigning variable came from an old JS)

console.log(varVariable);

let letVariable = 10;

letVariable++;

// let variable cannot be reassigned but it can be incremented

console.log(letVariable);

const constVariable = 10;

console.log(constVariable);
//const variable cannot be reassigned
